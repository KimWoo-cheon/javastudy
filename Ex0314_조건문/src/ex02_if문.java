
public class ex02_if문 {

	public static void main(String[] args) {
        
		int totalScore = 98;
		
		if (totalScore>=90) {
			System.out.print("A");}
		if (totalScore>=80) {
				System.out.print("B");}
		if (totalScore>=70) {
					System.out.print("C");}
		else {
						System.out.print("D");}
		
		System.out.println("학점 입니다!");
					
	}

}
