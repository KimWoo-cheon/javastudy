//		 			 
//		3. 조회
//		- 배열 : 변수명[인덱스]
//		- ArrayList : 변수명.get(인덱스)
//		
//		4. 삭제
//		-배열 : 삭제 개념x
//		-ArrayList  : 변수명.remove(인덱스) : 해당 인덱스번호의 값을 삭제
//					: 변수명.clear() : 전체삭제
//					
//		5. 크기
//		-배열 : 변수명.length
//		-ArrayList : 변수명.size()
