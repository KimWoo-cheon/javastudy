
public class ex02_연산자 {

	public static void main(String[] args) {
	
		int num1 = 10;// int형 공간인 num1에 데이터 10 저장하기!
		
		int num2 = 7;// num2 라는 변수에 정수형 데이터 7 저장하기!
		
		//연산하기 -> 결과 값 발생
		//num1 / num2; 오류 발생
		int result= num1 / num2;
		
		System.out.println(result); 
		System.out.println(result); //syso ctrl+space
		System.out.println(num1 % num2);
	}

}
