package for문;

public class Ex01For문 {

	public static void main(String[] args) {
		
		// for문
		// (초기화구문; 조건식; 반복 후 작업)
		
		for(int i = 0; i<=3; i++) {
			//i가 조건식을 만족할 동안 반복시킬 코드
			System.out.println(i);
		}
		
		//1~9까지 출력하는 for문
		for (int i = 1; i<=9 ; i++ ) {
		System.out.println(i);
		
		}
		int n=8;
		
		for (int i = 1; i<=n ; i++ ) {
			System.out.print("*");
			
			}
	}

}
